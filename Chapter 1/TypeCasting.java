//type casting is basically converting one type of variable to another

public class TypeCasting {
    public static void main(String[] args) {
    int in =  -5;
    float fl = in;
    System.out.println(in);
    System.out.println(fl);
    float flt = -5.67f;
    int meow = (int)flt;
    System.out.println(meow);

  
    }
    
}

//note that float got a decimal version, this is differnce between int and float for integers
//converting from float to int, ignores the decimal part